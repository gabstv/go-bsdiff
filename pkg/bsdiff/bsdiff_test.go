func TestOfftout(t *testing.T) {
	buf := make([]byte, 8)
	offtout(9001, buf)
	n := binary.LittleEndian.Uint64(buf)
	if n != 9001 {
		t.Fatal(n, "!=", 9001)
	}
	//
	offtout(9002, buf)
	n = binary.LittleEndian.Uint64(buf)
	if n != 9002 {
		t.Fatal(n, "!=", 9002)
	}
}
